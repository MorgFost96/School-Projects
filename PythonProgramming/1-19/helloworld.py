#Experimenting with Strings

#String Literal - String That Appears in Code Within Quotes
print( "Hello World!" )
print( 'Hello World!' )

print( "Don't Fear" )
print( '"Hamlet"' )

print( """I'm Reading "Hamlet" by Shakespeare.""" )
print( '''I'm Reading "Hamlet" by Shakespeare.''' )

#Escape Sequence with Strings
# \t is tab (4 spaces)
# \n is newline
# \a is bell
# \', \", \\
print( "\n\n\nNewlines" )
print( "\tTabbed Text" )

#Concatenating Strings
print( "one" + "two" )

#Repeated Strings
print( "pies" * 6 )       

